-- ---------- dim_product ----------
DROP TABLE IF EXISTS gold.dim_product CASCADE;
CREATE TABLE gold.dim_product (
    product_sk                   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id                   VARCHAR(64) NOT NULL,
    category_name_pt             VARCHAR(128),
    category_name_en             VARCHAR(128),
    product_name_length          INTEGER,
    product_description_length   INTEGER,
    product_photos_qty           INTEGER,
    product_weight_g             INTEGER,
    product_length_cm            INTEGER,
    product_height_cm            INTEGER,
    product_width_cm             INTEGER,
    volume_cm3                   NUMERIC(18, 2),
    weight_bucket                VARCHAR(16),
    size_bucket                  VARCHAR(16),
    etl_loaded_at                TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_product_id UNIQUE (product_id)
);

INSERT INTO gold.dim_product (product_sk, product_id, category_name_pt, category_name_en)
VALUES (-1, 'unknown', 'unknown', 'unknown');

INSERT INTO gold.dim_product (
    product_id, category_name_pt, category_name_en,
    product_name_length, product_description_length, product_photos_qty,
    product_weight_g, product_length_cm, product_height_cm, product_width_cm,
    volume_cm3, weight_bucket, size_bucket
)
SELECT
    p.product_id,
    COALESCE(NULLIF(TRIM(p.product_category_name), ''), 'unknown'),
    COALESCE(t.product_category_name_english, 'unknown'),
    p.product_name_lenght,
    p.product_description_lenght,
    p.product_photos_qty,
    p.product_weight_g,
    p.product_length_cm,
    p.product_height_cm,
    p.product_width_cm,
    CASE
        WHEN p.product_length_cm IS NOT NULL
         AND p.product_height_cm IS NOT NULL
         AND p.product_width_cm  IS NOT NULL
        THEN (p.product_length_cm::NUMERIC * p.product_height_cm * p.product_width_cm)
        ELSE NULL
    END AS volume_cm3,
    CASE
        WHEN p.product_weight_g IS NULL THEN 'unknown'
        WHEN p.product_weight_g < 500 THEN 'light'
        WHEN p.product_weight_g < 2000 THEN 'medium'
        ELSE 'heavy'
    END AS weight_bucket,
    CASE
        WHEN p.product_length_cm IS NULL OR p.product_height_cm IS NULL OR p.product_width_cm IS NULL THEN 'unknown'
        WHEN (p.product_length_cm::NUMERIC * p.product_height_cm * p.product_width_cm) < 2000 THEN 'small'
        WHEN (p.product_length_cm::NUMERIC * p.product_height_cm * p.product_width_cm) < 15000 THEN 'medium'
        ELSE 'large'
    END AS size_bucket
FROM bronze.stg_products p
LEFT JOIN bronze.stg_category_translation t
    ON TRIM(p.product_category_name) = TRIM(t.product_category_name);
