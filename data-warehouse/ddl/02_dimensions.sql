-- ---------- dim_date ----------
DROP TABLE IF EXISTS gold.dim_date CASCADE;
CREATE TABLE gold.dim_date (
    date_sk       INTEGER PRIMARY KEY,  -- YYYYMMDD
    full_date     DATE NOT NULL UNIQUE,
    year          SMALLINT NOT NULL,
    quarter       SMALLINT NOT NULL,
    month         SMALLINT NOT NULL,
    month_name    VARCHAR(16) NOT NULL,
    day_of_month  SMALLINT NOT NULL,
    day_of_week   SMALLINT NOT NULL,    -- 1=Mon .. 7=Sun (ISO)
    day_name      VARCHAR(16) NOT NULL,
    week_of_year  SMALLINT NOT NULL,
    year_month    CHAR(7) NOT NULL,     -- YYYY-MM
    is_weekend    BOOLEAN NOT NULL
);

-- Generate calendar 2016-01-01 .. 2019-12-31
INSERT INTO gold.dim_date (
    date_sk, full_date, year, quarter, month, month_name,
    day_of_month, day_of_week, day_name, week_of_year, year_month, is_weekend
)
SELECT
    TO_CHAR(d, 'YYYYMMDD')::INTEGER,
    d,
    EXTRACT(YEAR FROM d)::SMALLINT,
    EXTRACT(QUARTER FROM d)::SMALLINT,
    EXTRACT(MONTH FROM d)::SMALLINT,
    TRIM(TO_CHAR(d, 'Month')),
    EXTRACT(DAY FROM d)::SMALLINT,
    EXTRACT(ISODOW FROM d)::SMALLINT,
    TRIM(TO_CHAR(d, 'Day')),
    EXTRACT(WEEK FROM d)::SMALLINT,
    TO_CHAR(d, 'YYYY-MM'),
    EXTRACT(ISODOW FROM d) IN (6, 7)
FROM generate_series('2016-01-01'::DATE, '2019-12-31'::DATE, '1 day') AS d;

