-- ---------- dim_geography ----------
DROP TABLE IF EXISTS gold.dim_geography CASCADE;
CREATE TABLE gold.dim_geography (
    geo_sk          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    zip_code_prefix VARCHAR(8) NOT NULL,
    city            VARCHAR(128),
    state           CHAR(2),
    region          VARCHAR(32),
    latitude        DOUBLE PRECISION,
    longitude       DOUBLE PRECISION,
    CONSTRAINT uq_geo_zip UNIQUE (zip_code_prefix)
);

-- Unknown member
INSERT INTO gold.dim_geography (geo_sk, zip_code_prefix, city, state, region, latitude, longitude)
VALUES (-1, '00000', 'Unknown', 'XX', 'Unknown', NULL, NULL);

-- Load from bronze geolocation (aggregate multi lat/lng per zip)
INSERT INTO gold.dim_geography (zip_code_prefix, city, state, region, latitude, longitude)
SELECT
    LPAD(REGEXP_REPLACE(geolocation_zip_code_prefix, '[^0-9]', '', 'g'), 5, '0') AS zip_code_prefix,
    LOWER(TRIM(MAX(geolocation_city))) AS city,
    UPPER(TRIM(MAX(geolocation_state))) AS state,
    CASE UPPER(TRIM(MAX(geolocation_state)))
        WHEN 'AC' THEN 'North' WHEN 'AM' THEN 'North' WHEN 'AP' THEN 'North'
        WHEN 'PA' THEN 'North' WHEN 'RO' THEN 'North' WHEN 'RR' THEN 'North'
        WHEN 'TO' THEN 'North'
        WHEN 'AL' THEN 'Northeast' WHEN 'BA' THEN 'Northeast' WHEN 'CE' THEN 'Northeast'
        WHEN 'MA' THEN 'Northeast' WHEN 'PB' THEN 'Northeast' WHEN 'PE' THEN 'Northeast'
        WHEN 'PI' THEN 'Northeast' WHEN 'RN' THEN 'Northeast' WHEN 'SE' THEN 'Northeast'
        WHEN 'ES' THEN 'Southeast' WHEN 'MG' THEN 'Southeast' WHEN 'RJ' THEN 'Southeast'
        WHEN 'SP' THEN 'Southeast'
        WHEN 'PR' THEN 'South' WHEN 'RS' THEN 'South' WHEN 'SC' THEN 'South'
        WHEN 'DF' THEN 'Midwest' WHEN 'GO' THEN 'Midwest' WHEN 'MS' THEN 'Midwest'
        WHEN 'MT' THEN 'Midwest'
        ELSE 'Unknown'
    END AS region,
    AVG(geolocation_lat) AS latitude,
    AVG(geolocation_lng) AS longitude
FROM bronze.stg_geolocation
WHERE geolocation_zip_code_prefix IS NOT NULL
GROUP BY LPAD(REGEXP_REPLACE(geolocation_zip_code_prefix, '[^0-9]', '', 'g'), 5, '0')
ON CONFLICT (zip_code_prefix) DO NOTHING;

-- ---------- dim_customer ----------
DROP TABLE IF EXISTS gold.dim_customer CASCADE;
CREATE TABLE gold.dim_customer (
    customer_sk            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_unique_id     VARCHAR(64) NOT NULL,
    customer_zip_code_prefix VARCHAR(8),
    customer_city          VARCHAR(128),
    customer_state         CHAR(2),
    geo_sk                 INTEGER REFERENCES gold.dim_geography (geo_sk),
    etl_loaded_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_customer_unique UNIQUE (customer_unique_id)
);

INSERT INTO gold.dim_customer (
    customer_sk, customer_unique_id, customer_zip_code_prefix,
    customer_city, customer_state, geo_sk
) VALUES (-1, 'unknown', '00000', 'Unknown', 'XX', -1);

-- One row per customer_unique_id (latest zip/city/state by arbitrary pick)
INSERT INTO gold.dim_customer (
    customer_unique_id, customer_zip_code_prefix, customer_city, customer_state, geo_sk
)
SELECT
    c.customer_unique_id,
    c.zip_pad,
    c.customer_city,
    c.customer_state,
    COALESCE(g.geo_sk, -1)
FROM (
    SELECT DISTINCT ON (customer_unique_id)
        customer_unique_id,
        LPAD(REGEXP_REPLACE(customer_zip_code_prefix, '[^0-9]', '', 'g'), 5, '0') AS zip_pad,
        LOWER(TRIM(customer_city)) AS customer_city,
        UPPER(TRIM(customer_state)) AS customer_state
    FROM bronze.stg_customers
    WHERE customer_unique_id IS NOT NULL
    ORDER BY customer_unique_id, customer_id
) c
LEFT JOIN gold.dim_geography g ON g.zip_code_prefix = c.zip_pad;

